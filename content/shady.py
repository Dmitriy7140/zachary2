def hide_message(who: str, amount: int, iphone_blocked: bool) -> str:
    if iphone_blocked:
        pool = _HIDE_IPHONE
    else:
        tier = min(5, max(1, -(-amount // 1000)))  # ceil по тысячам
        pool = _HIDE_TIERS[tier]
    return random.choice(pool).format(who=who, amount=amount)


def iphone_trash(who: str) -> str:
    return random.choice(_IPHONE_TRASH).format(who=who)
