# --- итог обзвона в тред: без сумм, только масштаб навара ({who}) ---
_SCAM_TIER_SMALL = [   # до 200
    "📞 {who} обзванивал доверчивых граждан. Наварил ровно на жвачку.",
    "📞 {who} весь день висел на телефоне. Итог — купит теперь жвачку, может даже две.",
]
_SCAM_TIER_MID = [     # 200–400
    "📞 {who} солидно наварил на телефонных разводах. Голос — его оружие.",
    "📞 {who} отработал обзвон как надо — солидно наварил.",
]
_SCAM_TIER_BIG = [     # от 400
    "📞 {who} — гандон: столько бабок развёл, что даже не стыдно.",
    "📞 {who} развёл граждан на такую сумму, что даже гудки в трубке присвистнули. Гандон, конечно.",
]


def scam_chat(who: str, score: int) -> str:
    if score < 200:
        pool = _SCAM_TIER_SMALL
    elif score < 400:
        pool = _SCAM_TIER_MID
    else:
        pool = _SCAM_TIER_BIG
    return random.choice(pool).format(who=who)


def hint(count: int, target: int, character: str) -> str:
    diff = abs(count - target)
    direction = "больше слов" if count < target else "меньше слов"
    if diff == 1:
        temp = "🔥 Совсем рядом!"
    elif diff <= 3:
        temp = "🌡 Тепло."
    elif diff <= 7:
        temp = "❄️ Холодновато."
    else:
        temp = "🧊 Ледяной холод."
    return f"{temp} {character} мнётся — кажется, надо {direction}."
