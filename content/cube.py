def winner_announcement(
    winner: str,
    prize_amount: int,
    participant_count: int,
) -> str:
    """Публичный результат; ``winner`` уже должен быть HTML-safe."""
    return (
        "🧊🏆 <b>Куб взломан!</b>\n"
        f"Первым до призовой комнаты добрался {winner}.\n"
        f"Внутри было участников: <b>{participant_count}</b>.\n"
        f"Добыча: <b>{prize_amount} Z</b>.\n\n"
        "Стены уже перестроились. Новый Куб ждёт первую жертву."
    )


def lobby_invitation(generation_id: int) -> str:
    return (
        f"🧊 <b>Куб #{generation_id} перестроился</b>\n\n"
        "Набор снова открыт. Уведомление не держит место и ничего не "
        "списывает — кто раньше нажал, тот раньше услышал, как закрылась дверь."
    )


# Hazard description_key хранится отдельно от hazard_kind. Это позволяет
# read-only экрану безопасно отрисовать комнату даже без специальной ветки.
ROOM_DESCRIPTIONS.update(
    {
        f"hazard.{kind}": text.description
        for kind, text in HAZARD_TEXTS.items()
    }
)
