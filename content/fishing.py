"""Тексты рыбалки без удочки — над игроком ржут Пан Жмыжко и Павел Славянин."""
import random

NO_ROD = [
    "🎣 Ты пришёл рыбачить без удочки! Пан Жмыжко и Павел Славянин ржут: «Руками ловить будешь?»",
    "🎣 Без удочки на рыбалку?! Павел Славянин крутит у виска, Пан Жмыжко галантно подаёт тебе... ничего.",
    "🎣 Удочки нет. Пан Жмыжко предложил ловить рыбу на обаяние — не вышло. Славянин плачет со смеху.",
    "🎣 Ты машешь руками над водой. Славянин снимает это на телефон, Жмыжко уже отправил дамам.",
]
NO_ROD_CHAT = [
    "🎣 {player} приперся на рыбалку без удочки — Пан Жмыжко и Павел Славянин обхохотались.",
    "🎣 {player} пытался ловить рыбу голыми руками. Славянин и Жмыжко занесли это в анналы позора.",
    "🎣 {player} на рыбалке без снастей. Жмыжко покрутил ус, Славянин покрутил у виска.",
]


def no_rod() -> str:
    return random.choice(NO_ROD)


def no_rod_chat(player: str) -> str:
    return random.choice(NO_ROD_CHAT).format(player=player)
