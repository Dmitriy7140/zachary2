"""Случайные приветствия игроков."""
import random

_GREETINGS = [
    "🎮 {nick} ворвался на сервер. Прячьте детей и алмазы.",
    "👋 О, {nick} зашёл! Сервер только что стал на 10% опаснее.",
    "🍞 {nick} снова с нами. Кто-то опять не сделал уроки?",
    "⛏️ {nick} в деле. Алмазы, дрожите.",
    "🔥 {nick} залогинился. Эпоха разрушений началась.",
    "🧟 {nick} вошёл — мобы напряглись.",
    "🌟 Внимание! {nick} в игре. Это не учебная тревога.",
    "🐔 {nick} тут. Кто-то опять будет фармить курей вместо алмазов.",
]

_FIRST_TIME = [
    "🎉 Новенький на районе! {nick}, добро пожаловать.\nТы тут впервые — заводи профиль в ZakharCompanion 👇",
    "👀 Так-так-так, кто это у нас? {nick}, первый раз?\nЖми кнопку и стань своим 👇",
    "🆕 {nick} только что родился на сервере.\nЗарегистрируйся и начни фармить Zbucks 👇",
]


_FAREWELLS = [
    "👋 {nick} вышел. Сервер выдохнул.",
    "🚪 {nick} свалил — до новых встреч.",
    "💤 {nick} ушёл в офлайн. Без него как-то тихо.",
    "🏃 {nick} покинул сервер. Кто теперь будет фармить курей?",
    "🌚 {nick} отключился. F в чат.",
    "📉 {nick} вышел. Онлайн просел, настроение тоже.",
]


def random_greeting(nick: str) -> str:
    return random.choice(_GREETINGS).format(nick=nick)


def first_time_greeting(nick: str) -> str:
    return random.choice(_FIRST_TIME).format(nick=nick)


def random_farewell(nick: str) -> str:
    return random.choice(_FAREWELLS).format(nick=nick)
