def sale_good(reward: int) -> str:
    return random.choice(_SALE_GOOD).format(reward=reward)


def sale_ok(reward: int) -> str:
    return random.choice(_SALE_OK).format(reward=reward)


def no_sale() -> str:
    return random.choice(_NO_SALE)


def busted(bribe: int) -> str:
    return random.choice(_BUSTED).format(bribe=bribe)


def dodged() -> str:
    return random.choice(_DODGED)


def vpn_chat(who: str, score: int, was_busted: bool) -> str:
    if was_busted:
        return random.choice(_CHAT_BUSTED).format(who=who)
    pool = _CHAT_BIG if score >= 1000 else _CHAT_SMALL
    return random.choice(pool).format(who=who)
