"""Тексты для действий с предметами в инвентаре."""
import random

_MILK_SHAKE = [
    "Ну и нахуя ты это взболтал? Оно обезжиренное, масла из него хер получишь, дурила.",
    "Тряси не тряси — толку ноль. Обезжиренное оно, еба. Никакого масла, олух.",
    "Молодец, блядь, взболтал. Только оно обезжиренное — масло тебе не светит.",
]

_MILK_DRINK = [
    "Кислое пиздец, отдаёт мочой и сукровицей. Как будто козу прямо туда выжали.",
    "Фу, блядь. Тёплое, кислое, с привкусом хлева. Ты это правда пьёшь?",
    "На вкус как носок бабки Зины — кислятина с горчинкой мочи. Бееее.",
]

_BIKE_RIDE = [
    "Ты залез на велик и тут же навернулся мордой в асфальт. Нога хрустнула — страховая списала {take} Z.",
    "Три метра — и ты уже в кювете со сломанной ногой. Страховая забрала {take} Z.",
    "Педали крутанул, руль повело — перелом. Скорая, гипс, минус {take} Z со счёта.",
]

_ROD_WAVE = [
    "Крючок зацепился за штаны и порвал их нахуй. Нафиг ты ей машешь?",
    "Махнул удочкой — крючок впился в штаны и распорол их к чертям. Доволен?",
    "Леска захлестнула ногу, крючок разодрал портки. Зачем ты это вообще делаешь?",
]


def milk_shake() -> str:
    return random.choice(_MILK_SHAKE)


def milk_drink() -> str:
    return random.choice(_MILK_DRINK)


def bike_ride(take: int) -> str:
    return random.choice(_BIKE_RIDE).format(take=take)


def rod_wave() -> str:
    return random.choice(_ROD_WAVE)
