# масштаб стирки для треда: конкретные суммы не палим
def _launder_scale(amount: int) -> str:
    if amount <= 1000:
        return "Копейки какие-то."
    if amount <= 2000:
        return "Денежки-то водятся."
    if amount <= 3000:
        return "Солидный кэш."
    if amount <= 4000:
        return "Бабла немеренно."
    if amount <= 5000:
        return "Выносили мешками."
    return "Целая фура налички приехала."


