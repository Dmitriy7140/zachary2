def action_label(key: str) -> str:
    name, prop = PRODUCTS[key]
    return ACTION_LABELS[prop].format(name)


def wrong_action_label(key: str) -> str:
    """Неверный глагол для продукта — главный запутыватель."""
    name, prop = PRODUCTS[key]
    other = random.choice([p for p in ACTION_LABELS if p != prop])
    return ACTION_LABELS[other].format(name)


def build_round(recipe: dict) -> tuple[list[str], int]:
    """Собрать 3 варианта (перемешанные) и индекс правильного.

    Правильный: действие по свойству ПЕРВОГО продукта. Дистракторы:
    неверный глагол к первому продукту и верное действие к НЕ первому.
    """
    first = recipe["order"][0]
    correct = action_label(first)
    d1 = wrong_action_label(first)
    d2 = action_label(random.choice(recipe["order"][1:]))
    options = [correct, d1, d2]
    random.shuffle(options)
    return options, options.index(correct)


