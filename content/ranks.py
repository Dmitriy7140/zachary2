"""Звания по уровням — идут вместе с ником в приветствии/прощании.

Эмодзи по нарастающей крутости. Уровень выше максимума -> последнее звание.
"""

_RANKS = [
    ("🐣", "Новичок"),
    ("🔩", "Стажёр"),
    ("🔧", "Младший механик"),
    ("🛠️", "Механик"),
    ("📋", "Начальник смены"),
    ("🦺", "Бригадир"),
    ("🏭", "Начальник цеха"),
    ("📐", "Инженер-теоретик"),
    ("🎓", "Кандидат инженерных наук"),
    ("👨‍🔬", "Доктор инженерных наук"),
    ("⚛️", "Альберт Эйнштейн"),
]


def rank(level: int) -> str:
    """«эмодзи Звание» для уровня."""
    level = max(0, min(level, len(_RANKS) - 1))
    emoji, name = _RANKS[level]
    return f"{emoji} {name}"
